package task255;
//f. write a program you have to print the table of given number.

public class Task6 {
	
	public static void main(String[] args) 
	{	
		
        // multiplication table.
        int num = 15;
 
        // looping from 1 to 10 to print the multiplication
       
        for (int i = 1; i <= 10; i++) {
            System.out.println(num + " * " + i + " = "
                               + num * i);
        }
		
	}

}
